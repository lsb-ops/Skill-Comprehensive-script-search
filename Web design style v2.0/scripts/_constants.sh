# ============================================================
# A 类 — HTML 网页检查
# ============================================================
# A0: index.html 大小 (硬约束 50KB)
TDD_A0_MAX_HTML_BYTES=51200

# A2: 页数 (≥ 5 页)
TDD_A2_MIN_PAGE_COUNT=5

# A4: 字号 (≥ 24px)
TDD_A4_MIN_FONT_PX=24

# ============================================================
# B 类 — 抖音文案检查 (v1.2.0: 严格化)
# ============================================================
# B1: 标题 (硬约束 ≤ 22 字, 阈值严格 22)
TDD_B1_MAX_TITLE_LEN=22

# B2: 正文 (硬约束 80-150 字, 阈值严格 80-150)
TDD_B2_MIN_BODY_LEN=80
TDD_B2_MAX_BODY_LEN=150

# B3: hashtag (硬约束 5-8 个, 阈值严格 5-8)
TDD_B3_MIN_HASHTAG=5
TDD_B3_MAX_HASHTAG=8

# ============================================================
# C 类 — 时间线剧本检查
# ============================================================
# C2: 时间轴行数 (≥ 5 行)
TDD_C2_MIN_ROW_COUNT=5

# ============================================================
# G 类 — 剪映字幕检查
# ============================================================
# G6: 单段时长 (≤ 5s 硬约束)
TDD_G6_MAX_SEGMENT_DUR=5

# G7: 单段字符数 (≤ 30 字 硬约束)
TDD_G7_MAX_CHARS=30

# G8: 总时长 (硬约束 15-60s, 阈值用 13-92 含 ±2 缓冲)
TDD_G8_MIN_TOTAL_DUR=13
TDD_G8_MAX_TOTAL_DUR=92

# G10: 钩子段时长 (2-4s 区间)
TDD_G10_MIN_HOOK_DUR=2
TDD_G10_MAX_HOOK_DUR=4
